値の中には<b>プレースホルダー</b>を含めることができます。
プレースホルダーは<b>[SOMETEXT]</b>のように見え、<i>SOMETEXT</i>とは異なっています。.
いくつかの役に立つプレースホルダー:
</p>
<div class="placeholders">
<ul>
<li>WEBSITE - ウェブサイトのアドレス
<li>DOMAIN - ウェブサイトのドメイン
<li>SUBSCRIBEURL - 購読ページのロケーション
<li>UNSUBSCRIBEURL - 購読解除ページのロケーション
<li>BLACKLISTURL - the location of the unsubscribe page for unknown users</li>
<li>PREFERENCESURL - ユーザが詳細を更新することができるページのロケーション
<li>CONFIRMATIONURL - ユーザが購読を確定しなければいけないページのロケーション
</ul>
